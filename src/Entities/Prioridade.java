package Entities;

/**
 * Enumeração que define os níveis de prioridade atribuíveis a uma tarefa.
 * 
 * <p>Os níveis são classificados de acordo com a urgência da execução:</p>
 * <ul>
 *   <li><strong>ALTA</strong> — execução prioritária e imediata;</li>
 *   <li><strong>MEDIA</strong> — importância intermédia;</li>
 *   <li><strong>BAIXA</strong> — tarefas menos urgentes.</li>
 * </ul>
 */
public enum Prioridade {
    ALTA,       
    MEDIA,      
    BAIXA       
}